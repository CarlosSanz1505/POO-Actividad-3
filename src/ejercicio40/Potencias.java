package ejercicio40;

public class Potencias {
    public static double raizCuadrada(double a) {
        return Math.sqrt(a);
    }
    
    public static double Cuadrado(double a) {
        return Math.pow(a,2);
    }
    
    public static double Cubo(double a) {
        return Math.pow(a,3);
    }
}
